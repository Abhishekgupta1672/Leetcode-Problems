class Solution {
  public:
    string removePair(string s) {
        string ans = "";
        for(auto x : s){
            if(ans.empty() || ans.back() != x){
                ans += x;
            }
            else ans.pop_back();
        }
        return ans.size()?ans:"-1";
    }
};


